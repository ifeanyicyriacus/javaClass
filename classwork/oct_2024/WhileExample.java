public class WhileExample{
	public static void main(String[] args){
		
		for(int counter = 1; counter <= 10; counter++){
		  System.out.print(counter);
		}
		System.out.println();
		
		int counters = 1;
		for( ; counters <= 10; ){
		  System.out.print(counters);
		  counters++;
		}
		System.out.println();
		
	}
}
